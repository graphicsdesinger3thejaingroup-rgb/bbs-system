"""
Professional PDF report generator using ReportLab Platypus.

Produces a multi-page client-ready document:

    Page 1     — COVER PAGE
                  Project name · Client · Date · Company · (optional logo)
    Page 2     — PROJECT DETAILS
                  Element type, dimensions, cover, bar details
    Page 3+    — BBS TABLE
                  Paginates if long. Bar mark / Element / Description / Dia /
                  Cut Len / Qty / Total Len / Weight
    Page 4+    — BAR DRAWINGS
                  One drawing per bar mark with dimensions & shape labels
    Final page — STEEL ORDER SUMMARY
                  Dia-wise grouping, totals, 5% wastage, 12 m rod count

Every page carries:
    - Header  : company name + report title
    - Footer  : page number + "Generated by BBS System"
    - Watermark: "FOR CONSTRUCTION USE" diagonal, faint grey
"""

from datetime import datetime
import os
from typing import Dict, Any, Optional

from reportlab.lib import colors
from reportlab.lib.pagesizes import A4
from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
from reportlab.lib.units import mm, cm
from reportlab.lib.enums import TA_CENTER, TA_LEFT
from reportlab.pdfgen.canvas import Canvas
from reportlab.platypus import (BaseDocTemplate, PageTemplate, Frame,
                                Paragraph, Spacer, Table, TableStyle,
                                PageBreak, Image, KeepTogether)
from reportlab.platypus.flowables import HRFlowable

from services.bar_drawings import make_drawing_for_row


# ---------------- Theme ----------------

PRIMARY    = colors.HexColor("#1F4E78")
PRIMARY_2  = colors.HexColor("#2E7CB6")
TEXT       = colors.HexColor("#1F2937")
MUTED      = colors.HexColor("#6B7280")
ROW_ALT    = colors.HexColor("#EAF1F8")
SUCCESS    = colors.HexColor("#2E7D32")
DANGER     = colors.HexColor("#C62828")

PAGE_W, PAGE_H = A4
MARGIN_L = 18 * mm
MARGIN_R = 18 * mm
MARGIN_T = 22 * mm
MARGIN_B = 22 * mm

WATERMARK_TEXT = "FOR CONSTRUCTION USE"
COMPANY_DEFAULT = "BBS Automation"
GENERATED_BY    = "Generated by BBS System"


# ---------------- Page decoration callback ----------------

def _draw_page_decorations(canv: Canvas, doc):
    """Draw watermark, header & footer on EVERY page (called by ReportLab)."""
    canv.saveState()

    project_name = getattr(doc, "_project_name", "Bar Bending Schedule")
    company_name = getattr(doc, "_company_name", COMPANY_DEFAULT)
    page_num = canv.getPageNumber()

    # --------- Watermark (very faint, diagonal) ---------
    canv.saveState()
    canv.translate(PAGE_W / 2, PAGE_H / 2)
    canv.rotate(45)
    canv.setFont("Helvetica-Bold", 60)
    canv.setFillColor(colors.HexColor("#1F4E78"))
    canv.setFillAlpha(0.06)
    canv.drawCentredString(0, 0, WATERMARK_TEXT)
    canv.restoreState()

    # --------- Header ---------
    canv.setStrokeColor(PRIMARY)
    canv.setLineWidth(0.5)
    canv.line(MARGIN_L, PAGE_H - 14 * mm, PAGE_W - MARGIN_R, PAGE_H - 14 * mm)

    canv.setFont("Helvetica-Bold", 9)
    canv.setFillColor(PRIMARY)
    canv.drawString(MARGIN_L, PAGE_H - 11 * mm, company_name)

    canv.setFont("Helvetica", 9)
    canv.setFillColor(MUTED)
    canv.drawRightString(PAGE_W - MARGIN_R, PAGE_H - 11 * mm,
                         project_name)

    # --------- Footer ---------
    canv.setStrokeColor(MUTED)
    canv.setLineWidth(0.3)
    canv.line(MARGIN_L, 14 * mm, PAGE_W - MARGIN_R, 14 * mm)

    canv.setFont("Helvetica", 8)
    canv.setFillColor(MUTED)
    canv.drawString(MARGIN_L, 9 * mm, GENERATED_BY)
    canv.drawCentredString(PAGE_W / 2, 9 * mm,
                           f"IS 456:2000 · IS 2502:1963 compliant")
    canv.drawRightString(PAGE_W - MARGIN_R, 9 * mm, f"Page {page_num}")

    canv.restoreState()


